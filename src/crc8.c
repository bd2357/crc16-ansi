// This can be used at runtime to calculate a new table
const uint8_t * CalulateTable_CRC8(uint8_t generator)
{
    //const byte generator = 0x1D;
    static uint8_t crctable[256];
    /* iterate over all byte values 0 - 255 */
    for (int divident = 0; divident < 256; divident++)
    {
        uint8_t currByte = (uint8_t)divident;
        /* calculate the CRC-8 value for current byte */
        for (int bit = 0; bit < 8; bit++)
        {
            if ((currByte & 0x80) != 0)
            {
                currByte <<= 1;
                currByte ^= generator;
            }
            else
            {
                currByte <<= 1;
            }
        }
        /* store CRC value in lookup table */
        crctable[divident] = currByte;
    }
    return crctable;
} 

void crc8Init(uint8_t gen)
{
    crc8Table = CalulateTable_CRC8(gen);
}

#endif

uint8_t calcCrc8(uint8_t lastCrc, uint8_t *buf, size_t bufLen)
{
    for(int i=0; i<bufLen; i++)
    {
        lastCrc = crc8Table[buf[i] ^ lastCrc];
    }
    return lastCrc;
}

uint8_t crc8place(uint8_t val)
{
    return val;
}



